package Day04;

public class Driver {
	//1. 매개변수를 부모객체로 선언하므로서
	//   다양한 자식객체들을 매개변수로 받을 수 있게 되고
	//   매개변수로 받은 자식객체들에 따라 결과값이 달라짐
	//   bus가 Vihicle형태로 형변환
	//   매개변수가 Vihicle객체 형태
	//   매개변수에 Vihicle의 형태를 가지고 있는 객체는 모두 가능
	//   Vihicle, Bus, Taxi
	public void drive(Vihicle vihicle) {
		vihicle.run();
	}
	
	public int add(int a, int b) {
		return a + b;
	}
}
